double starsBetween(const vector<Stars>& v,vector<string>& names, const double& lower, const double& upper)
{
	int amountOfStarsBeingPassed = 0;

	double actualAvg = 0;

	for (const auto e : v) //We only calculate avg magnitudes if the stars fall between the two parameters
	{

		if (e.magnitude >= lower && e.magnitude <= upper)//If any of the stars qualify/ valid than we add to the amount of stars applicable to avg
		{
			++amountOfStarsBeingPassed; 
			actualAvg += e.magnitude;
			names.push_back(e.names);
		}

	}
	double average = actualAvg / amountOfStarsBeingPassed;

	return average;

}


vector<Stars> longestHop( vector<Stars>& v)
{
    //Goal is to iterate through each star via their x and y points; distance formula and find the farthest 2 stars.
    //1. Create a prototype vector to return
    vector<Stars> currentLongest; // Since looking for furthest two stars, the total amount in this vector should be two

    //2. Start looping through Each star and its next,
    //we can use a for, while

    double prevDistance = 0;
    for (size_t i = 0; i < v.size() - 1; i++)
    {
        double distanceFormula =  sqrt((pow((v[i + 1].x - v[i].x), 2.0) + pow((v[i + 1].y - v[i].y), 2.0))); // Current Longest

        if (distanceFormula > 0 && distanceFormula > prevDistance) // sets biggest distance
        {
            currentLongest.clear();
            prevDistance = distanceFormula; // sets newest prevDistance that is also the biggest
            currentLongest.push_back(v[i]);
            currentLongest.push_back(v[i+1]);

        }
    }
    return currentLongest;
}

